CREATE OR REPLACE FORCE VIEW VW_DW_TRANS_B_ELEMENTS
(  POINTCODE,
   PROCESSID,
   ELEMENTTYPE,
   ELEMENTSUBTYPE,
   CORRECTIONDIRECTION,
   LOADPERCENTAGE,
   LOSSCONSTANTPERCENTAGE,
   TRANSFORMERTYPE,
   ACTIVITYTYPE,
   TITULARPOWER,
   VACUUMLOSSES,
   CHARGINGLOSSES,
   VACUUMCURRENT,
   CURRENTVOLTAGE,
   RESISTANCE,
   REACTANCE,
   LENGTHLINE,
   NOMINALTENSION,
   CAPACITY,
   SPECIFICLOSSES,
   FUNCTIONINGTIME,
   FUNCTIONINGTIMEHOURS,
   YEAR,
   ENERGYCHANNEL,
   CONVCONSUMPTIONS,
   STARTDATE,
   ENDDATE,
   EXTRACTIONDATE,
   TIMEZONE )
AS
SELECT
      POINTCODE,
	  PROCESSID,
	  ELEMENTTYPE,
	  ELEMENTSUBTYPE,
	  CORRECTIONDIRECTION,
	  LOADPERCENTAGE,
	  LOSSCONSTANTPERCENTAGE,
	  TRANSFORMERTYPE,
	  ACTIVITYTYPE,
	  TITULARPOWER,
	  VACUUMLOSSES,
	  CHARGINGLOSSES,
	  VACUUMCURRENT,
	  CURRENTVOLTAGE,
	  RESISTANCE,
	  REACTANCE,
	  LENGTHLINE,
	  NOMINALTENSION,
	  CAPACITY,
	  SPECIFICLOSSES,
	  FUNCTIONINGTIME,
	  FUNCTIONINGTIMEHOURS,
	  YEAR,
	  ENERGYCHANNEL,
	  CONVCONSUMPTIONS,
	  TO_CHAR (STARTDATE, 'DD/MM/YYYY HH24:MI:SS')      STARTDATE,
	  TO_CHAR (ENDDATE,   'DD/MM/YYYY HH24:MI:SS')      ENDDATE,
	  TO_CHAR (EXTRACTIONDATE, 'DD/MM/YYYY HH24:MI:SS') EXTRACTIONDATE,
	  TIMEZONE
 FROM DW_TRANS_B_ELEMENTS
WHERE CURRENT_FLAG='Y';
